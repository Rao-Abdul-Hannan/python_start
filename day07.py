language = "python"

# string indexing

print (language[2])
print (language[-1])  #starts from end 5 - 6 = -1

# string slicing 
# syntax -> language[start argument - end argument]

print (language[2 : 4])
print (language[-2 : 6]) # ulta nhi ja skty
print (language[:])
print (language[1 : ])
print (language[ : -1])

# syntax -> language[start argument - end argument]

print ("Abdul-Hannan"[1 : 3])
print ("Abdul-Hannan"[0 : 5 : 3])
print ("Abdul-Hannan"[5 :  : -1]) # reverse string will be printed
print ("Abdul-Hannan"[-1 :  : -1])
print ("Abdul-Hannan"[5 :  : 1])